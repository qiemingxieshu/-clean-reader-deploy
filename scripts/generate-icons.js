const fs = require('fs');
const { createCanvas } = require('canvas');

// 创建 512x512 图标
function createIcon(size, filename) {
  const canvas = createCanvas(size, size);
  const ctx = canvas.getContext('2d');

  // 背景
  ctx.fillStyle = '#4a90d9';
  ctx.beginPath();
  ctx.roundRect(0, 0, size, size, size * 0.15);
  ctx.fill();

  // 书本图标（简化版）
  ctx.fillStyle = 'white';
  const bookWidth = size * 0.5;
  const bookHeight = size * 0.6;
  const bookX = (size - bookWidth) / 2;
  const bookY = (size - bookHeight) / 2 + size * 0.1;

  // 书页
  ctx.fillRect(bookX + bookWidth * 0.1, bookY, bookWidth * 0.8, bookHeight);
  
  // 书脊
  ctx.fillStyle = '#357abd';
  ctx.fillRect(bookX + bookWidth * 0.1, bookY, bookWidth * 0.1, bookHeight);

  // 文字线条
  ctx.fillStyle = '#e0e0e0';
  for (let i = 0; i < 5; i++) {
    ctx.fillRect(
      bookX + bookWidth * 0.25,
      bookY + bookHeight * 0.15 + i * bookHeight * 0.15,
      bookWidth * 0.6,
      bookHeight * 0.05
    );
  }

  canvas.toBuffer((err, buffer) => {
    if (err) throw err;
    fs.writeFileSync(filename, buffer);
    console.log(`Created ${filename}`);
  });
}

